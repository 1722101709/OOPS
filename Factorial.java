import java.lang.*;
public class Factorial {
	public static void main(String[] args) {
		int a=7;
		System.out.println("factorial of "+a+"  is ");
		int b=1;
		for(int i=1;i<=a;i++)
		{
			b*=i;
		}
		System.out.println(b);
	}
}